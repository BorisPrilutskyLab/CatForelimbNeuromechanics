        'Teres major', 'Triceps brachii lateralis', 'Triceps brachii long', ...
        'Triceps brachii medial', 'Teres minor' ...
    };

    uniqueMuscles = unique(cellfun(@(x) regexprep(x, ' O-R| R-I', ''), muscles, 'UniformOutput', false));
    [indx, tf] = listdlg('ListString', uniqueMuscles, 'SelectionMode', 'single', 'PromptString', 'Select a muscle for visualization in 2D and 3D:', 'ListSize', [300, 600]);
    
    if tf == 1
        selectedMuscle = uniqueMuscles{indx};
        matchingRows = find(contains(muscles, selectedMuscle));
        selectedRow = min(matchingRows);
        fprintf('Selected Muscle Visualization in 2D and 3D: %s\n', selectedMuscle);
        fprintf('Row Number: %d\n', selectedRow);
    else
        disp('No muscle selected.');
        selectedRow = [];
    end
end
