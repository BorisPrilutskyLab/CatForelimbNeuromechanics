        'Flexor digitorum superficialis 4 O-R', 'Flexor digitorum superficialis 4 R-I', ...
        'Flexor digitorum superficialis 5 O-R', 'Flexor digitorum superficialis 5 R-I', ...
        'Infraspinatus O-R', 'Infraspinatus R-I', 'Palmaris longus 1 O-R', ...
